// 2779. Maximum Beauty of an Array After Applying Operation

pub fn maximum_beauty(nums: Vec<i32>, k: i32) -> i32 {
    let mut nums = nums;
    nums.sort_unstable();

    let mut res = 0;
    let mut l = 0;
    for r in 0..nums.len() {
        while nums[r] - nums[l] > 2 * k {
            l += 1;
        }
        res = res.max(r - l + 1);
    }
    res as _
}

